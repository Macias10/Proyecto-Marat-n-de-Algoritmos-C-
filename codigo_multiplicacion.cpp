#include <iostream>
using namespace std;

int main() {
    // Declaración de variables
    float num1, num2, resultado;

    // Entrada de datos
    cout << "Ingrese el primer número: ";
    cin >> num1;
    cout << "Ingrese el segundo número: ";
    cin >> num2;

    // Proceso: multiplicación
    resultado = num1 * num2;

    // Salida de resultado
    cout << "El resultado de la multiplicación es: " << resultado << endl;

    return 0;
}
